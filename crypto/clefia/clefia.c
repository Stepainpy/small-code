#include "clefia.h"
#include <limits.h>
#include <string.h>
#include <stdlib.h>

#   if UCHAR_MAX == 0xFF
typedef unsigned char clefia_byte_t;
# else
#error Not found 8-bit integer
#endif

#   if  UINT_MAX == 0xFFFFFFFF
typedef unsigned int  clefia_word_t;
# elif ULONG_MAX == 0xFFFFFFFF
typedef unsigned long clefia_word_t;
# else
#error Not found 32-bit integer
#endif

typedef clefia_byte_t clefia_brick_t[4];
typedef clefia_word_t clefia_block_t[4];
typedef clefia_word_t clefia_chunk_t[8];

struct clefia_context_t {
    clefia_word_t WK[ 4];
    clefia_word_t RK[52];
    clefia_word_t rounds;
};

#define                    clefiai_gf_by_1( x ) (x)
static const clefia_byte_t clefiai_gf_by_2[256];
static const clefia_byte_t clefiai_gf_by_4[256];
static const clefia_byte_t clefiai_gf_by_6[256];
static const clefia_byte_t clefiai_gf_by_8[256];
static const clefia_byte_t clefiai_gf_by_a[256];

static const clefia_byte_t clefiai_S0[256];
static const clefia_byte_t clefiai_S1[256];

static const clefia_word_t clefiai_CON128[60];
static const clefia_word_t clefiai_CON192[84];
static const clefia_word_t clefiai_CON256[92];

static void clefiai_write_to_brick(clefia_brick_t out, const clefia_word_t in) {
    out[0] = (in >> 24) & 0xFF;
    out[1] = (in >> 16) & 0xFF;
    out[2] = (in >>  8) & 0xFF;
    out[3] = (in >>  0) & 0xFF;
}

static clefia_word_t clefiai_read_from_brick(const clefia_brick_t in) {
    clefia_word_t out = 0;
    out |= (clefia_word_t)in[0] << 24;
    out |= (clefia_word_t)in[1] << 16;
    out |= (clefia_word_t)in[2] <<  8;
    out |= (clefia_word_t)in[3] <<  0;
    return out;
}

static clefia_word_t clefiai_F0(clefia_word_t X, clefia_word_t RK) {
    clefia_brick_t T, M;
    clefiai_write_to_brick(T, X ^ RK);

    T[0] = clefiai_S0[T[0]];
    T[1] = clefiai_S1[T[1]];
    T[2] = clefiai_S0[T[2]];
    T[3] = clefiai_S1[T[3]];

    M[0] = clefiai_gf_by_1(T[0]) ^ clefiai_gf_by_2[T[1]] ^ clefiai_gf_by_4[T[2]] ^ clefiai_gf_by_6[T[3]];
    M[1] = clefiai_gf_by_2[T[0]] ^ clefiai_gf_by_1(T[1]) ^ clefiai_gf_by_6[T[2]] ^ clefiai_gf_by_4[T[3]];
    M[2] = clefiai_gf_by_4[T[0]] ^ clefiai_gf_by_6[T[1]] ^ clefiai_gf_by_1(T[2]) ^ clefiai_gf_by_2[T[3]];
    M[3] = clefiai_gf_by_6[T[0]] ^ clefiai_gf_by_4[T[1]] ^ clefiai_gf_by_2[T[2]] ^ clefiai_gf_by_1(T[3]);

    return clefiai_read_from_brick(M);
}

static clefia_word_t clefiai_F1(clefia_word_t X, clefia_word_t RK) {
    clefia_brick_t T, M;
    clefiai_write_to_brick(T, X ^ RK);

    T[0] = clefiai_S1[T[0]];
    T[1] = clefiai_S0[T[1]];
    T[2] = clefiai_S1[T[2]];
    T[3] = clefiai_S0[T[3]];

    M[0] = clefiai_gf_by_1(T[0]) ^ clefiai_gf_by_8[T[1]] ^ clefiai_gf_by_2[T[2]] ^ clefiai_gf_by_a[T[3]];
    M[1] = clefiai_gf_by_8[T[0]] ^ clefiai_gf_by_1(T[1]) ^ clefiai_gf_by_a[T[2]] ^ clefiai_gf_by_2[T[3]];
    M[2] = clefiai_gf_by_2[T[0]] ^ clefiai_gf_by_a[T[1]] ^ clefiai_gf_by_1(T[2]) ^ clefiai_gf_by_8[T[3]];
    M[3] = clefiai_gf_by_a[T[0]] ^ clefiai_gf_by_2[T[1]] ^ clefiai_gf_by_8[T[2]] ^ clefiai_gf_by_1(T[3]);

    return clefiai_read_from_brick(M);
}

static void clefiai_rotl_block(clefia_block_t T) {
    clefia_word_t first = T[0];
    memmove(T, T + 1, 12);
    T[3] = first;
}

static void clefiai_rotr_block(clefia_block_t T) {
    clefia_word_t last = T[3];
    memmove(T + 1, T, 12);
    T[0] = last;
}

static void clefiai_rotl_chunk(clefia_chunk_t T) {
    clefia_word_t first = T[0];
    memmove(T, T + 1, 28);
    T[7] = first;
}

static void clefiai_rotr_chunk(clefia_chunk_t T) {
    clefia_word_t last = T[7];
    memmove(T + 1, T, 28);
    T[0] = last;
}

static void clefiai_GFN4_fwd(clefia_block_t out, const clefia_block_t in, const clefia_word_t* RK, int rounds) {
    clefia_block_t T; int i;
    memcpy(T, in, 16);
    for (i = 0; i < rounds; i++) {
        T[1] ^= clefiai_F0(T[0], RK[2 * i + 0]);
        T[3] ^= clefiai_F1(T[2], RK[2 * i + 1]);
        clefiai_rotl_block(T);
    }
    clefiai_rotr_block(T);
    memcpy(out, T, 16);
}

static void clefiai_GFN8_fwd(clefia_chunk_t out, const clefia_chunk_t in, const clefia_word_t* RK, int rounds) {
    clefia_chunk_t T; int i;
    memcpy(T, in, 32);
    for (i = 0; i < rounds; i++) {
        T[1] ^= clefiai_F0(T[0], RK[4 * i + 0]);
        T[3] ^= clefiai_F1(T[2], RK[4 * i + 1]);
        T[5] ^= clefiai_F0(T[4], RK[4 * i + 2]);
        T[7] ^= clefiai_F1(T[6], RK[4 * i + 3]);
        clefiai_rotl_chunk(T);
    }
    clefiai_rotr_chunk(T);
    memcpy(out, T, 32);
}

static void clefiai_GFN4_inv(clefia_block_t out, const clefia_block_t in, const clefia_word_t* RK, int rounds) {
    clefia_block_t T; int i;
    memcpy(T, in, 16);
    for (i = 0; i < rounds; i++) {
        T[1] ^= clefiai_F0(T[0], RK[2 * (rounds - i) - 2]);
        T[3] ^= clefiai_F1(T[2], RK[2 * (rounds - i) - 1]);
        clefiai_rotr_block(T);
    }
    clefiai_rotl_block(T);
    memcpy(out, T, 16);
}

#if __BYTE_ORDER__ == __ORDER_LITTLE_ENDIAN__
static clefia_word_t clefiai_bswap(clefia_word_t x) {
    x = (x & 0xFFFF0000) >> 16 | (x & 0x0000FFFF) << 16;
    x = (x & 0xFF00FF00) >>  8 | (x & 0x00FF00FF) <<  8;
    return x;
}
#endif

static void clefiai_write_to_block(clefia_block_t dst, const void* src) {
    memcpy(dst, src, 16);
#if __BYTE_ORDER__ == __ORDER_LITTLE_ENDIAN__
    dst[0] = clefiai_bswap(dst[0]);
    dst[1] = clefiai_bswap(dst[1]);
    dst[2] = clefiai_bswap(dst[2]);
    dst[3] = clefiai_bswap(dst[3]);
#endif
}

static void clefiai_read_from_block(void* dst, const clefia_block_t src) {
    clefia_block_t T;
    memcpy(T, src, 16);
#if __BYTE_ORDER__ == __ORDER_LITTLE_ENDIAN__
    T[0] = clefiai_bswap(T[0]);
    T[1] = clefiai_bswap(T[1]);
    T[2] = clefiai_bswap(T[2]);
    T[3] = clefiai_bswap(T[3]);
#endif
    memcpy(dst, T, 16);
}

static void clefiai_perm_block(clefia_block_t out, const clefia_block_t in) {
    clefia_block_t T; memcpy(T, in, 16);
    out[0] =  T[0]               <<  7 |  T[1]         >> 25;
    out[1] =  T[1]               <<  7 | (T[3] & 0x7F)      ;
    out[2] = (T[0] & 0xFE000000)       |  T[2]         >>  7;
    out[3] =  T[2]               << 25 |  T[3]         >>  7;
}

static void clefiai_init_key128(clefia_context_t* ctx, const void* key) {
    clefia_block_t K, L, T; int i;

    clefiai_write_to_block(K, key);

    ctx->rounds = 18;
    ctx->WK[0] = K[0];
    ctx->WK[1] = K[1];
    ctx->WK[2] = K[2];
    ctx->WK[3] = K[3];

    clefiai_GFN4_fwd(L, K, clefiai_CON128, 12);
    for (i = 0; i <= 8; i++) {
        T[0] = L[0] ^ clefiai_CON128[24 + 4 * i + 0];
        T[1] = L[1] ^ clefiai_CON128[24 + 4 * i + 1];
        T[2] = L[2] ^ clefiai_CON128[24 + 4 * i + 2];
        T[3] = L[3] ^ clefiai_CON128[24 + 4 * i + 3];

        clefiai_perm_block(L, L);

        if (i & 1) {
            T[0] ^= K[0];
            T[1] ^= K[1];
            T[2] ^= K[2];
            T[3] ^= K[3];
        }

        ctx->RK[4 * i + 0] = T[0];
        ctx->RK[4 * i + 1] = T[1];
        ctx->RK[4 * i + 2] = T[2];
        ctx->RK[4 * i + 3] = T[3];
    }
}

static void clefiai_init_key192(clefia_context_t* ctx, const void* key) {
    clefia_block_t KL, KR, LL, LR, T;
    clefia_chunk_t K, L; int i;

    clefiai_write_to_block(KL, key);
    clefiai_write_to_block(KR, (const char*)key + 8);
    KR[0] = KR[2]; KR[2] = ~KL[0];
    KR[1] = KR[3]; KR[3] = ~KL[1];

    ctx->rounds = 22;
    ctx->WK[0] = KL[0] ^ KR[0];
    ctx->WK[1] = KL[1] ^ KR[1];
    ctx->WK[2] = KL[2] ^ KR[2];
    ctx->WK[3] = KL[3] ^ KR[3];

    memcpy(&K[0], KL, 16);
    memcpy(&K[4], KR, 16);
    clefiai_GFN8_fwd(L, K, clefiai_CON192, 10);
    memcpy(LL, &L[0], 16);
    memcpy(LR, &L[4], 16);

    for (i = 0; i <= 10; i++) {
        if (i % 4 < 2) {
            T[0] = LL[0] ^ clefiai_CON192[40 + 4 * i + 0];
            T[1] = LL[1] ^ clefiai_CON192[40 + 4 * i + 1];
            T[2] = LL[2] ^ clefiai_CON192[40 + 4 * i + 2];
            T[3] = LL[3] ^ clefiai_CON192[40 + 4 * i + 3];
            clefiai_perm_block(LL, LL);
            if (i & 1) {
                T[0] ^= KR[0];
                T[1] ^= KR[1];
                T[2] ^= KR[2];
                T[3] ^= KR[3];
            }
        } else {
            T[0] = LR[0] ^ clefiai_CON192[40 + 4 * i + 0];
            T[1] = LR[1] ^ clefiai_CON192[40 + 4 * i + 1];
            T[2] = LR[2] ^ clefiai_CON192[40 + 4 * i + 2];
            T[3] = LR[3] ^ clefiai_CON192[40 + 4 * i + 3];
            clefiai_perm_block(LR, LR);
            if (i & 1) {
                T[0] ^= KL[0];
                T[1] ^= KL[1];
                T[2] ^= KL[2];
                T[3] ^= KL[3];
            }
        }

        ctx->RK[4 * i + 0] = T[0];
        ctx->RK[4 * i + 1] = T[1];
        ctx->RK[4 * i + 2] = T[2];
        ctx->RK[4 * i + 3] = T[3];
    }
}

